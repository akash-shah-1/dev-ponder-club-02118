import { Dialog, DialogContent, DialogHeader, DialogTitle, DialogDescription } from "@/components/ui/dialog";
import { Badge } from "@/components/ui/badge";
import { Sparkles, FileText } from "lucide-react";

interface SummaryModalProps {
  open: boolean;
  onOpenChange: (open: boolean) => void;
  summary: string;
  generatedAt: string;
}

// Simple markdown renderer for summary
const renderSummary = (text: string) => {
  const parts = text.split('\n\n');
  
  return parts.map((part, index) => {
    // Headers
    if (part.startsWith('## ')) {
      return <h2 key={index} className="text-lg font-bold mt-4 mb-2">{part.replace('## ', '')}</h2>;
    }
    
    // Lists
    if (part.includes('\n- ')) {
      const items = part.split('\n').filter(line => line.startsWith('- '));
      return (
        <ul key={index} className="list-disc list-inside space-y-1 ml-4 mb-3">
          {items.map((item, iIndex) => (
            <li key={iIndex}>{item.replace('- ', '')}</li>
          ))}
        </ul>
      );
    }
    
    // Regular paragraphs
    return part.trim() ? (
      <p key={index} className="leading-relaxed mb-3">{part}</p>
    ) : null;
  });
};

export const SummaryModal = ({ open, onOpenChange, summary, generatedAt }: SummaryModalProps) => {
  return (
    <Dialog open={open} onOpenChange={onOpenChange}>
      <DialogContent className="max-w-2xl max-h-[80vh] overflow-y-auto">
        <DialogHeader>
          <div className="flex items-center gap-2 mb-2">
            <div className="p-2 bg-gradient-to-br from-blue-600 to-indigo-600 rounded-lg">
              <FileText className="h-5 w-5 text-white" />
            </div>
            <div className="flex-1">
              <DialogTitle className="text-xl">AI-Generated Summary</DialogTitle>
              <DialogDescription className="text-xs">
                Generated {new Date(generatedAt).toLocaleString()}
              </DialogDescription>
            </div>
            <Badge variant="secondary" className="gap-1 bg-blue-100 text-blue-700 dark:bg-blue-900 dark:text-blue-300">
              <Sparkles className="h-3 w-3" />
              AI Summary
            </Badge>
          </div>
        </DialogHeader>

        <div className="prose prose-sm max-w-none text-sm">
          {renderSummary(summary)}
        </div>

        <div className="mt-4 pt-4 border-t">
          <p className="text-xs text-muted-foreground italic">
            ğŸ’¡ This summary was generated by AI to help you quickly understand the discussion.
          </p>
        </div>
      </DialogContent>
    </Dialog>
  );
};
